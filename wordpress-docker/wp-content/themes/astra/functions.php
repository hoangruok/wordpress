add_filter('wp_nav_menu_items', 'replace_user_info_in_menu', 10, 2);
function replace_user_info_in_menu($items, $args) {
    if (is_user_logged_in()) {
        $current_user = wp_get_current_user();
        $username = esc_html($current_user->display_name);
        $avatar = get_avatar($current_user->ID, 24); // 24 là kích thước ảnh avatar

        // Thay thế các placeholder
        $items = str_replace('%%username%%', $username, $items);
        $items = str_replace('%%user-avatar%%', $avatar, $items);
    } else {
        // Nếu chưa đăng nhập thì ẩn avatar, hoặc dùng icon mặc định
        $items = str_replace('%%username%%', 'Khách', $items);
        $items = str_replace('%%user-avatar%%', '', $items);
    }
    return $items;
}
add_shortcode('khoa_hoc_key_form', function () {
    ob_start();

    if (isset($_POST['khoa_hoc_key'])) {
        $key = sanitize_text_field($_POST['khoa_hoc_key']);
        $user_id = get_current_user_id();

        // Danh sách mã và ID khóa học tương ứng
        $access_keys = [
            'NC' => 62,
        ];

        if (isset($access_keys[$key])) {
            $course_id = $access_keys[$key];

            // Ghi danh nếu chưa ghi danh trước đó
            if (!tutor_utils()->is_enrolled($course_id, $user_id)) {
                tutor_utils()->enroll($course_id, $user_id);
                echo '<div style="color: green;">✅ Bạn đã được ghi danh vào khóa học!</div>';
            } else {
                echo '<div style="color: orange;">⚠️ Bạn đã ghi danh khóa học này rồi.</div>';
            }

            // Chuyển hướng sau 2 giây
            echo "<script>setTimeout(function() {
                window.location.href = '" . get_permalink($course_id) . "';
            }, 2000);</script>";
        } else {
            echo '<div style="color: red;">❌ Mã truy cập không hợp lệ.</div>';
        }
    }

    ?>
    <form method="post">
        <label for="khoa_hoc_key">Nhập mã truy cập khóa học:</label><br>
        <input type="text" name="khoa_hoc_key" required>
        <button type="submit">Tham gia</button>
    </form>
    <?php

    return ob_get_clean();
});
