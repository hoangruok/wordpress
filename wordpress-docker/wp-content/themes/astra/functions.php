add_action('template_redirect', 'handle_course_activation_key');

function handle_course_activation_key() {
    if (is_page('activate-course') && isset($_GET['activate_key'])) {
        global $wpdb;
        $key = sanitize_text_field($_GET['activate_key']);
        $user_id = get_current_user_id();

        $row = $wpdb->get_row($wpdb->prepare("
            SELECT * FROM {$wpdb->prefix}course_activation_keys
            WHERE activation_key = %s AND is_used = 0
        ", $key));

        if ($row && $user_id == $row->user_id) {
            if (function_exists('tutor_utils')) {
                tutor_utils()->enroll($row->course_id, $user_id);
            }

            $wpdb->update("{$wpdb->prefix}course_activation_keys", ['is_used' => 1], ['id' => $row->id]);

            wp_redirect(home_url('/course-activated?success=1'));
            exit;
        } else {
            wp_redirect(home_url('/course-activated?error=1'));
            exit;
        }
    }
}
